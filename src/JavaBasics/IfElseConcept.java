package JavaBasics;

public class IfElseConcept {

    public static void main(String[] args) {
        int a = 10 ;
        int b = 20 ;

        if(a>b){
            System.out.println("b is greater then b"); // if the (if) statement is correct then this statement will be printed
        }
        else{
            System.out.println("a is greater then b"); // if the (if) statement is incorrect then this statement will be printed
        }

    }
}
