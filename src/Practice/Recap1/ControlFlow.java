package Practice.Recap1;

public class ControlFlow {

    public static void main(String[] args) {

        //While Loop;

        int vName = 0; // initialization

        while(vName<=10){  // condition or termination
            System.out.println(vName);
            vName++; // increment or decrement
        }


        for ( int b =0 ; b<=15; b++){ // increment statement
            System.out.println(b);
        }

        for (int a =10 ; a>=0 ; a--){ // decrement statement
            System.out.println(a);
        }

        for ( int c=10; c>=-2; c--){
            System.out.println(c);
        }



    }
}
