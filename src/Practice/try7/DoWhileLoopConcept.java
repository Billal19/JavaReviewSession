package Practice.try7;

public class DoWhileLoopConcept {

    public static void main(String[] args) {
        int i =4;
        do {
            System.out.println(i);
            i++;
        }while (i>10);

        // Do while loop is a variant of while loop , the loop will execute the block of code once before checking if the condition is true , then the loop will repeat
        // the execution as long as the condition is true , in do while loop , the loop executes the block of code at least once even if the condition is false .
    }

}
